public class ExtractionItem {
    private int id;
    public String name;
    public int taka;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getTaka() {
        return taka;
    }

    public void setTaka(int taka) {
        this.taka = taka;
    }
}
